int	ft_sqrt(int nb)
{
	int	i;
	int	result;

	i = 1;
	result = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	while (result <= nb)
	{
		i++;
		result = i * i;
	}
	if ((i -1) * (i - 1) != nb)
		return (0);
	return (i - 1);
}
